class CalculatorImpl implements Calculator {
    @Override
    Number plus(Object a, Object b) {
        a+b
    }

    @Override
    Number minus(Object a, Object b) {
        a-b
    }

    @Override
    Number multy(Object a, Object b) {
        a*b
    }

    @Override
    Number divide(Object a, Object b) {
        a/b
    }
}
